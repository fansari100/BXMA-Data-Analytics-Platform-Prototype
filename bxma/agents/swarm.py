"""
Risk Swarm: Multi-Agent System for Autonomous Risk Management
=============================================================

Implements a hierarchical swarm of specialized agents that collaborate
to monitor, analyze, and manage portfolio risk autonomously.

Architecture:
- Architect: Central orchestrator breaking down mandates into sub-goals
- Analysts: Domain-specific agents (Vol, Credit, Macro, etc.)
- Judge: Non-LLM compliance firewall validating all actions

Author: BXMA Quant Team
Date: January 2026
"""

